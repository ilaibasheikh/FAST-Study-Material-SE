#include <stdio.h>
int main()
{
	printf("5 + 4 = %d \n", 5 + 4); //this is basic addition
	
	printf("10 / 2 = %d \n", 10/2); //this is basic divison
	
	printf("True OR False = %d \n", 1 || 0);  // true or false is true 
	
	printf("20 MOD 3 = %d \n", 20%3); //this will give us the remainder
	
	printf("5 < 8 = %d \n", 5 < 8); //this will compare the two numbers and see if its true or false
	
	printf("25 MOD 70 = %d \n", 25%70); //this will give us the remainder
	
	printf("NOT True = %d \n", !1); //this will give the opposite of true
	
	printf("25 / 70 = %f \n", 25/70); //this is basic divison
	
	printf("False AND True = %d \n", 0 && 1); // this will mulitply false and true to give false
	
	printf("20 * 0.5 = %f \n", 20*0.5); // this is basic multiplication
	
	printf("35 <= 35 = %d \n", 35<=35); //this will compare the two values
	
	return 0;
}